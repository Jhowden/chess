# Board is 8 x 8 grid. Row is identified by letters a-h starting at the left. Column is identified by numbers 1-8 starting at bottom.

# Pieces consist of 1 king, 1 queen, 2 rooks, 2 bishops, 2 knights, and 8 pawns. Queens start to the left of kings when behind board.

# White always goes first.

# Pieces can't go off the board

# King movement:
### exactly one square horizontally, vertically, or diagonally
# Queen movement:
### moves any number of vacant squares horizontally, vertically, or diagonally
# Rook movement:
### moves any number of vacant squares in a horizontal or vertical direction
# Bishop movement:
### moves any number of vacant squares in any diagonal direction
# Knight movement:
### moves to the nearest square not on the same rank, file, or diagonal ("L" pattern, 2 vertical, 1 horizontal OR 1 vertical, 2 horizontal)
# Pawn movement:
### moves straight forward one square if that square is vacant. If it has not moved, pawn can move two squares straight forward if both squares are vacant. Pawns can capture pieces that are one square diagonally in front of them.

# Special moves:
## Castling
### King moves two squares towards a rook, and then the rook is placed on the other side of the king.
#### can only be performed if king and rook have not yet been moved, no pieces between king and rook, the king cannot be in check, or pass through or end up in a square that is under attack by an enemy piece, and the king and rook are on the same rank
## En passant
### A pawn that moves forward two squares and has an opposing pawn on an adjacent file can be captured as if the original pawn had moved only one square. The opposing pawn then moves diagonally.
#### This capture can only occur immediately subsequent the move.
## Pawn promotion
### If a pawn reaches the opposite side, the pawn can be promoted to a queen, rook, bishop, or knight. There are no limits to this.

# Check
## A player may not make any move that places or leaves his/her king in check.

# Checkmate
## If a king is placed in check and there is no legal move to escape, the kind is said to be checkmated.

# Draw
## A king is not in check but has no legal move.
